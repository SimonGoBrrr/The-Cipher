var countDownDate = new Date("Jul 25, 2022 16:37:52").getTime();; //You must complete before this time.


setInterval(function() {
    const total = countDownDate - Date.parse(new Date());
    const seconds = Math.floor( (total/1000) % 60 );
    const minutes = Math.floor( (total/1000/60) % 60 );
    const hours = Math.floor( (total/(1000*60*60)) % 24 );
    const days = Math.floor( total/(1000*60*60*24) );
    document.getElementById("clock").innerHTML = days.toString() + " : " + hours.toString() + " : " + minutes.toString() + " : " + seconds.toString();
}, 1000);


